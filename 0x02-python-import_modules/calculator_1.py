def add(a, b):
    """ Function add
    Args:
        a: first number
        b: sec number

    Return:
        a + b
    """
    return a + b

def sub(a, b):
    """ Function sub
    Args:
        a: first number
        b: second number
    Return:
        a - b
    """
    return a - b

def mul(a, b):
    """ Function mul
    Args:
        a: first num
        b: sec num
    Return:
        a * b
    """
    return a * b

def div(a, b):
    """ Function div
    Args:
        a: first num
        b: sec num
    Return:
        a * b
    """
    return a / b
